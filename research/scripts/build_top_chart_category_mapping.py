def app_ref(item: dict[str, str | int]) -> str:
    return f"#{item['rank']} {item['name']} ({item['price']})"


def table_rows(items: list[dict[str, str | int]]) -> list[str]:
    lines = ["| Rank | App | Price | Store category | Mapped category | Core replaceability | AI-agent readiness | Skill/tool fit | Why |"]
    lines.append("|---:|---|---|---|---|---|---|---|---|")
    for item in items:
        lines.append(
            "| {rank} | {name} | {price} | {store_category} | {mapped_category} | "
            "{replaceability} | {ai_agent_readiness} | {skill_or_tool_fit} | {reason} |".format(**item)
        )
    return lines


def write_markdown(rows_by_chart: dict[str, list[dict[str, str | int]]]) -> None:
    grouped: dict[str, dict[str, list[dict[str, str | int]]]] = defaultdict(lambda: defaultdict(list))
    for chart, rows in rows_by_chart.items():
        for item in rows:
            grouped[str(item["mapped_category"])][chart].append(item)

    lines = [
        "# Top 100 App Category Mapping",
        "",
        "Snapshot date: 2026-06-24.",
        "",
        "This is the full row-level mapping, not examples. The free Mac chart returned 98 rows for the requested top 100; the paid overall and paid Productivity charts are mapped through rank 100.",
        "",
        "Source boundaries:",
        "",
        "- Free overall: parsed local Mac free chart feed.",
        "- Paid overall: Val's Mac App Store UI screenshots plus the existing OCR-assisted transcription.",
        "- Paid Productivity: Val's Mac App Store UI screenshots plus the existing OCR-assisted transcription.",
        "- `Open`, `Open/Purchased`, or `View` in the price column means the storefront hid the price behind the account state.",
        "",
        "Method:",
        "",
        "- Category means the app's job-to-be-done, not only Apple's App Store category.",
        "- `yes` means the core workflow can be handled by a skill/tool with transparent files and approval gates.",
        "- `partial` means an agent can replace batch/report/content workflows but not the live UI, account, service, or resident app behavior.",
        "- `no` means the app depends on a network, hardware, identity, real-time UI, or trust model that a coding-agent skill should not replace.",
        "- `unknown` means the parsed name did not carry enough product signal for a defensible replacement call.",
        "- AI-agent readiness is conservative: it records whether the app/category has a native MCP/agent surface, API/connector surface, CLI/script surface, browser automation path, partial file automation path, low readiness, or is not a fit.",
        "",
        "## Category Rollup",
        "",
        "| Mapped category | Free overall apps | Paid overall apps | Paid Productivity apps |",
        "|---|---|---|---|",
    ]

    for category in sorted(grouped):
        free = "<br>".join(app_ref(item) for item in grouped[category].get("free_overall", [])) or "-"
        paid = "<br>".join(app_ref(item) for item in grouped[category].get("paid_overall", [])) or "-"
        productivity = "<br>".join(app_ref(item) for item in grouped[category].get("paid_productivity", [])) or "-"
        lines.append(f"| {category} | {free} | {paid} | {productivity} |")

    lines.extend(["", "## Free Overall Top 98", ""])
    lines.extend(table_rows(rows_by_chart["free_overall"]))
    lines.extend(["", "## Paid Overall Top 100", ""])
    lines.extend(table_rows(rows_by_chart["paid_overall"]))
    lines.extend(["", "## Paid Productivity Top 100", ""])
    lines.extend(table_rows(rows_by_chart["paid_productivity"]))
    lines.append("")

    target = ROOT / "research" / "top-100-category-mapping.md"
    target.write_text("\n".join(lines))


def main() -> None:
    rows = build_rows()
    write_json(rows)
    write_paid_source_json(rows)
    write_markdown(rows)


if __name__ == "__main__":
    main()
