def classify(name: str) -> Category:
    key = EXACT_CATEGORY.get(name)
    if key:
        return CATEGORY_META[key]
    lowered = name.lower()
    for needle, category in [
        ("vpn", "network_security"),
        ("pdf", "pdf_document_tools"),
        ("gmail", "email_calendar_meetings"),
        ("calendar", "email_calendar_meetings"),
        ("youtube", "browser_extension_or_web_wrapper"),
        ("safari", "browser_extension_or_web_wrapper"),
        ("browser", "browser_extension_or_web_wrapper"),
        ("chatbot", "ai_chat_or_writing"),
        ("ai ", "ai_chat_or_writing"),
        ("widget", "system_utility"),
        ("clipboard", "clipboard_automation"),
        ("markdown", "notes_tasks_writing_app"),
        ("timer", "timer_focus_utility"),
        ("recorder", "transcription_audio_notes"),
        ("screen", "presentation_screen_tools"),
        ("converter", "video_media_batch"),
    ]:
        if needle in lowered:
            return CATEGORY_META[category]
    return CATEGORY_META["unknown_or_low_signal"]


def readiness_for(name: str, category_label: str) -> AgentReadiness:
    return READINESS_BY_APP.get(name, READINESS_BY_CATEGORY[category_label])


def row(chart: str, rank: int, name: str, price: str, store_category: str) -> dict[str, str | int]:
    category = classify(name)
    readiness = readiness_for(name, category.label)
    return {
        "chart": chart,
        "rank": rank,
        "name": name,
        "price": price,
        "store_category": store_category,
        "mapped_category": category.label,
        "replaceability": category.replaceability,
        "skill_or_tool_fit": category.skill_fit,
        "reason": category.reason,
        "ai_agent_readiness": readiness.level,
        "ai_agent_readiness_basis": readiness.basis,
        "ai_agent_readiness_evidence_key": readiness.evidence_key,
    }


def build_rows() -> dict[str, list[dict[str, str | int]]]:
    paid_price_by_name = {name: price for _, name, price in PAID_OVERALL_TOP}
    return {
        "free_overall": [
            row("free_overall", rank, name, price, store_category)
            for rank, name, price, store_category in FREE_TOP
        ],
        "paid_overall": [
            row("paid_overall", rank, name, price, "Paid Overall")
            for rank, name, price in PAID_OVERALL_TOP
        ],
        "paid_productivity": [
            row(
                "paid_productivity",
                rank,
                name,
                PRODUCTIVITY_PRICE_OVERRIDES.get(name, paid_price_by_name.get(name, "not captured")),
                "Productivity",
            )
            for rank, name in PAID_PRODUCTIVITY_TOP
        ],
    }


